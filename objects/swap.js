let a = 5;
let b = 10;

function swap(a, b) {
    let tmp = a;
    a = b;
    b = tmp;

    document.write(`a=${a}, b=${b} <br>`);
    // Προσθέτουμε μια συνθήκη τερματισμού για να αποφύγουμε τον ατέλειωτο βρόγχο
    if (a !== 5 || b !== 10) {
        swap(a, b);
    }
    // Εδώ πρέπει να χρησιμοποιήσετε τα ανάποδα τελικά αντί για τα αρχικά `a` και `b`
    document.write(`a=${a}, b=${b}`);
}

// Καλούμε τη συνάρτηση swap με τις αρχικές τιμές των μεταβλητών a και b
swap(a, b);
